    private void jBremoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBremoverActionPerformed
         DefaultTableModel tableModel = (DefaultTableModel) jTablePedido.getModel();//Pega o modelo
         int row = jTablePedido.getSelectedRow(); //Seleciona a linha 
         String c,q,atual;
         
         c = tableModel.getValueAt(row, 1).toString(); //Pegar código
         q = tableModel.getValueAt(row, 2).toString(); //Pegar quantidade
         
         //double qA = Double.parseDouble(q), atualA = Double.parseDouble(atual),totalA = Double.parseDouble(total) ;
        Estoque e = new Estoque();
        EstoqueDAO pesq = new EstoqueDAO();
        e = pesq.consultarProduto(Integer.parseInt(c));
        atual = (String.valueOf(e.getQuantidade()));
        double qA = Double.parseDouble(q), atualA = Double.parseDouble(atual),total;
        total = atualA + qA;                
        
        e.setQuantidade(total); //Passando os valores para a alteração no produto
        e.setCodigo(Integer.parseInt(c));      
        
        pesq.qtdProduto(e);
      
        ((DefaultTableModel) jTablePedido.getModel()).removeRow(jTablePedido.getSelectedRow());
        
    }//GEN-LAST:event_jBremoverActionPerformed

