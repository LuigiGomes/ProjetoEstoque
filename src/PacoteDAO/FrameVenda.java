    private void jBinserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBinserirActionPerformed
        double qa,qv,qn,v;
        v = 0;
        qa = Double.parseDouble(jLestoque.getText());
        qv = Double.parseDouble(jTquantidade.getText());
        
        if(qa == v){
            JOptionPane.showMessageDialog(null,"Não há " + jTnome.getText() + " no estoque!");
            jTnome.setText(null);
            jTcodigo.setText(null);
            jTquantidade.setText(null);
            jLvalor.setText(null);
            jLestoque.setText(null);
            jBpesquisarC.setVisible(true);
            jBpesquisarN.setVisible(true);
            jTnome.requestFocus();}
        
        else if(qa < qv){
            JOptionPane.showMessageDialog(null,"Quantidade especificada de " + jTnome.getText() + " é maior do que o total disponível no estoque!");
            jTquantidade.requestFocus();
    } 
           else{
         
        qn = qa - qv;   //subtração para definir o novo valor do estoque
        
        Estoque e = new Estoque();
        e.setQuantidade(qn);
        e.setCodigo(Integer.parseInt(jTcodigo.getText()));        
        
        EstoqueDAO c = new EstoqueDAO();
        c.qtdProduto(e);
        
        String nome = jTnome.getText().trim();
        String codigo = jTcodigo.getText().trim();
        String quantidade = jTquantidade.getText().trim();
        
        DefaultTableModel val = (DefaultTableModel) jTablePedido.getModel();
        
            val.addRow(new String [] {nome,codigo,quantidade}); //adiciona valores a tabela
        
        jTnome.setText(null);
        jTcodigo.setText(null);
        jTquantidade.setText(null);
        jLvalor.setText(null);
        jLestoque.setText(null);
        jBpesquisarC.setVisible(true);
        jBpesquisarN.setVisible(true);
        jTnome.requestFocus();}
 
    }//GEN-LAST:event_jBinserirActionPerformed

